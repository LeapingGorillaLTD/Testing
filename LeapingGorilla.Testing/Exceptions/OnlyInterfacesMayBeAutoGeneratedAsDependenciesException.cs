using System;

namespace LeapingGorilla.Testing.Exceptions
{
	///<summary>Exception raised if a field or property is marked as a [Dependency] or [Mock] but is not an interface. </summary>
	public class OnlyInterfacesMayBeAutoGeneratedAsDependenciesException : ApplicationException
	{
		/// <summary>
		/// Initializes a new instance of the <see cref="OnlyInterfacesMayBeAutoGeneratedAsDependenciesException"/> class.
		/// </summary>
		/// <param name="typeToFake">The type to fake.</param>
		public OnlyInterfacesMayBeAutoGeneratedAsDependenciesException(Type typeToFake)
			: base(
				String.Format(
					"The type {0} cannot be used as an auto-generated dependency or mock as it is not an interface. You must manually set a value for this dependency by overriding CreateManualDependencies.",
					typeToFake))
		{
			
		}
	}
}
